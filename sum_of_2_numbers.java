import java.util.*;

public class sum_of_2_numbers{
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter the 1st Number :-");
        int a = sc.nextInt();
        System.out.println("Enter the 2nd Number :-");
        int b = sc.nextInt();

        int sum = a+b;
        System.out.println(sum);
    }   
    
}